                <p> In the recent past, there has been increase in production of gem quality synthetic stones. There has also been rise in cases of instances faced by buyers finding that the diamonds they had purchased as believing to be Natural Diamonds were actually discovered later to be Synthetic Diamonds. In light of the seriousness of such allegations, the GJEPC saw it as a critical responsibility to study the issue of undisclosed mixing in more detail and then outline any actions that may be required to address it. With this in mind, the Natural Diamond Monitoring Committee (NDMC) was formed at GJEPC. NDMC consists of representatives from the GJEPC, BDB, GJF, GIA and other Trade Bodies in the Gems & Jewellery sector.</p>
                
                <p> In 2015, NDMC had undertaken a studyto effectively address the issue of undisclosed mixing. </p>
                
