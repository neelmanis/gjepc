				<p>चूंकि, जेम एंड ज्वैलरी इंडस्ट्री के लिए यह जरूरी है कि वह उस सिंथेटिक डायमंड पर ज्ञान और जानकारी लगातार रखे, जिसका मौजूदा कई तकनीकियों के जरिए पता लगाया जाता है, जिस उद्देश्य पर जीजेईपीसी ने <b>भारत डायमंड बोर्स के साथ डायमंड डिटेक्शन एक्सपो एंड सिम्पोजियम (डीडीईएस 2015) को को मुंबई में ऑर्गनाइज किया था।</b></p>

				<p>डीडीईएस 2015 के सफलतम संस्करण के बाद, जीजेईपीसी ने 14 से 15 अप्रैल 2017 को समस्त पाटीदार समाज ट्रस्ट, सूरत में डायमंड डिटेक्शन एक्स्पो एंड सिम्पोजियम (डीडीईएस 2017) को दूसरी बार आर्गनाइज किया था।</p>
				
				<p><b>सूरत ही क्यों? </b><br>
क्योंकि, सूरत दुनिया के सबसे बड़े हीरे का विनिर्माण केंद्र है। डीडीईएस 2017 को ऑर्गनाइज करने के लिए आदर्श स्थान है। इसकी वजह है कि इस शहर में डायमंड मैन्युफैक्चरिंग की 5000 यूनिट हैं, इनमें दुनिया की सबसे बड़ी और सबसे एडवांस डायमंड कटिंग फैक्टरीज शामिल हैं। इसके अलावा शहर और उसके आसपास के इलाकों में 5600 से ज्यादा डायमंड मैन्युफैक्चर्स हैं। सूरत तेजी से ज्वैलरी प्रॉडक्शन हब के तौर पर उभर रहा है, क्योंकि यहं करीब 500 बड़ी, मध्यम और छोटी और ज्वैलरी मैन्युफैक्चरिंग कंपनियां हैं। गुजरात के दूसरे बड़े शहर के तौर पर, देश का सबसे तेजी से बढ़ता बिजनेस हब (व्यापारिक केंद्र) और सर्वसुविधा संपन्न वाणिज्यिक केंद्र में से एक है। सूरत की मुंबई, दिल्ली, जयपुर और देश के दूसरे मेट्रो से एयर और रेल की कनेक्टिविटी काफी अच्छी है। यह खुद अपना अडानी-हजारिया मॉडर्न पोर्ट बना रहा है।</p>

