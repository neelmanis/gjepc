			     <p>हाल के दिनों में, जेम्स क्वालिटी सिंथेटिक स्टोन्स के उत्पादन में बढ़ोत्तरी हुई है। ऐसे मामले भी बढ़ रहे हैं, जिनमें खरीदार को बाद में पता चलता है कि उसने नेचुरल डायमंड विश्वास कर जो डायमंड (हीरा) खरीदे थे, वे असल में सिंथेटिक (कृत्रिम) डायमंड थे। ऐसे गंभीर आरोप सामने आने के बाद, जीजेईपीसी (जीजेईपीसी) ने इसे अहम जिम्मेदारी माना और ज्यादा गहराई में जाकर पता नहीं चलने वाले मिश्रण (अज्ञात मिश्रण) के मामलों का अध्ययन किया। ऐसी कार्रवाई को तय किया, जो इन मामलों से निपटने के लिए जरूरी हो सकती हैं। यह ध्यान में रखते हुए, नेचुरल डायमंड मॉनिटरिंग कमेटी (एनडीएमसी) ने जीजेईपीसी का गठन किया। जेम्स एंड ज्वैलरी सेक्टर में एनडीएमसी में जीजेईपीसी, बीडीबी, जीजेएफ, जीआईए और दूसरी ट्रेड बॉडीज शामिल हैं। </p>

				<p>2015 में, एनडीएमसी ने अज्ञात मिश्रण के मुद्दे से असरदार तरीके से निपटने के लिए एक अध्ययन किया था।</p>
				
				<p>अध्ययन के प्रमुख सुझावों में से एक उद्योग के लिए पहचान तकनीक में सुधार करना था। मुख्य उद्देश्य तकनीकी अन्वेषण और स्वीकार करने को अलग-अलग करना है। </p>

