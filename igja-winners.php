      $('html,body').animate({scrollTop:$(this).offset().top-150});
    }).on('hide.bs.collapse', function(){
      $(this).prev(".card-header").find(".fa").removeClass("fa-minus").addClass("fa-plus");
    });
 $(window).load(function(){
