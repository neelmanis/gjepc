                <p> In the global gems and jewellery industry, India stands unique.</p>

<p>Be it in terms of sheer size or the vast range of skills and products, there is no other centre that comes anywhere close to what India has to offer today.</p>

<p>India, which gave the world its first diamonds many centuries ago, continues to manufacture 14 out of 15 diamonds set in jewellery even today.</p>

<p>The overall gem and jewellery exports (gross) from India in 2018-19 amounted to $40billion. This impressive export performance is built on a stable, well-developed and technologically advanced manufacturing base.</p>

<p class="blue">Largest Diamond Manufacturer</p>

<p>India's massive diamond manufacturing sector employs nearly one million people across the country. Though it initially concentrated only on small diamonds, India now cuts and polishes stones of all sizes and shapes.</p>

<p>Most of the manufacturing activity is centred on the Western coast of India in Gujarat, especially Surat and is carried out by a highly skilled workforce in large, modern factories having the latest high-tech machinery and equipment. Surat has the largest concentration of computerised diamond planners and state-of-the-art laser machines in the world; with several factories having between 100 and 500 lasers in a single complex.	</p>

<p>The industry has grown from its relatively small beginnings in the 1950s to emerge as the world's largest manufacturing centre of cut and polished diamonds. India today manufactures over 65 per cent of the world's polished diamonds in terms of value, 85 per cent in terms of volume and 92 per cent in terms of number of pieces.</p>

<p class="blue">Towards Trading</p>

<p>The Bharat Diamond Bourse, the modern, new trading complex that began operations in 2010 is the largest bourse in the world, and accounts for nearly 90 per cent of India's total diamond import and exports. It has helped the country take significant strides towards emerging as a leading diamond trading centre as well.</p>

<p class="blue">Gemstones, Jewellery and More</p>

<p>India also has a long tradition in the manufacturing of jewellery and coloured gemstones.</p>

<p>Jaipur, which is the hub of the coloured gemstone trade, is the world's largest manufacturer of many different gemstones including tanzanite and emeralds, among others.</p>

<p>The modern jewellery segment, especially for diamond-studded products, was initially centred in Mumbai's Special Economic Zone, SEEPZ. The factories compared well with the best in the world in terms of technology, skills and environment;they were,at first,set up mainly by diamantaires. Today, modern jewellery manufacturers have also opened units outside the zone in centres like Mumbai's MIDC and other parts of India like Surat SEZ in Surat, Noida SEZ in Delhi, Sitapura SEZ in Jaipur, Manikanchan SEZ in Kolkata, etc. to cater to India's growing share of the international market.</p>

<p>The jewellery segment is now mature and fully developed with highly talented designers and skilled artisans, creating the widest possible range of jewellery. From mass produced diamond jewellery designed for Europe or USA, to exquisitely crafted designer jewellery in plain gold or studded with diamonds and coloured gemstones, India offers it all.</p>

<p class="blue">Commitment to Quality</p>

<p>While the industry's rapid strides in the business domain are relatively well known, the keen attention to ethical and socially responsible business practices has remained a relatively lesser known facet. All large manufacturer-exporters have invested in establishing better facilities for workers and integrating best business practices into their functioning. India was a founder member of the Kimberley Process Certification Scheme to prevent conflict diamonds from entering the system, and participates in and supports all global initiatives to strengthen ethical, sustainable and responsible business practices.</p>

<p>Indian companies and trade bodies have also been deeply involved in charitable and philanthropic activities over the past few decades through trusts set up for the purpose. The spectrum includes education, health, women's welfare, protection of the environment, relief activities to victims of disasters and a host of other initiatives designed to improve the lives of people.</p>

