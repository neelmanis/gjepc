<p><span class="blue">Two Way SMS verification*: </span>This is a key feature which is important for verification of the artisan in case of emergency where he needs to prove his identity in case if he is interrogated by police/customs/government officers while carrying any gold, precious raw material or jewellery. In such cases he can prove his identity by presenting his Parichay Card to the interrogating officer, who can verify its authenticity by sending an SMS to a mobile number present on the card. A two-way SMS will be sent automatically to the device verifying the identity of the artisanproving that the Parichay Cars is genuine and reliable.</p>

