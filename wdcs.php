       		<p>Here is a quick reckoner of the World Diamond Council’s (WDC’s) System of Warranties (SoW), which offer a guide to the industry for self-monitoring.</p>
            
            
            <p> <strong> What is the System of Warranties? </strong> </p>
            
            <p> The current System of Warranties is created by the World Federation of Diamond Bourses (WFDB) and International Diamond Manufacturers Association (IDMA) (through WDC) in 2002 to comply, support and strengthen the Kimberley Process Certification Scheme (KPCS). It is an industry self-regulation, wherein invoices of all trades in rough diamonds, polished diamonds and jewellery containing diamonds should have the SoW declaration. </p>
            
            <p> The KPCS is designed to exclude conflict rough diamonds, and SoW system was put in place to ensure that benefits of the KP in the rough are extended up to the end consumer, without similar regulation. </p>
            
            <p> Under the SoW, each company is obliged to:</p>
            
            <ul class="inner_under_listing">
            	<li>Keep a record of all warranty invoices received and warranty invoices issued</li>
                <li>Flow of warranties in and warranties out to be audited and reconciled on an annual basis by the companies’ auditors</li>
                <li>If asked by an authorised government agency, records must be able to prove that you comply with the Kimberley Process</li>
            </ul>
            
            <p> <strong> Additionally, industry organisations (i.e. WFDB and hence Bharat Diamond Bourse, the Gem & Jewellery Export Promotion Council, etc.) have adopted the following: </strong></p>
            
             <ul class="inner_under_listing">
            	<li>Only to trade with companies who include the warranty declaration</li>
<li>Not to buy diamonds from suspect sources and those which originate in countries that have not implemented the KPCS</li>
<li>Not to buy diamonds from companies who have violated regulations on trade in conflict diamonds</li>
<li>Not to buy diamonds from any region subject to an advisory unless it is through the KPCS</li>
<li>Not to knowingly sell or assist others in selling conflict diamonds</li>
<li>To ensure that employees who buy/sell diamonds are well informed regarding the regulations restricting the trade in conflict diamonds</li>

            </ul>
            
            <p> <strong> Why is the declaration being changed? </strong></p>
            
            <p>The new SoW Guidelines were adopted last year based on external market needs.</p>
            
             <ul class="inner_under_listing">
            	<li>Address the emerging risks and address the product integrity of the global diamond industry</li>
<li>Respecting the universal standards for responsible business practices, human rights and diamond sourcing processes</li>
<li>Requirement from government and regulatory agencies for including other compliances as part of the System of Warranties declaration</li>
<li>Desire from consumers and NGO’s demanding greater responsibility in the diamond supply chain</li>

            </ul>
            
            <p>The SoW Guidelines recommended an enhanced warranty declaration on invoices which can be voluntarily adopted by companies.</p>
            
            <p> <strong> New System of Warranties Declaration </strong> </p>
            
            <p> Old definition: </p>
            
            <p> “The diamonds herein invoiced have been purchased from legitimate sources not involved in funding conflict and in compliance with United Nations resolutions. The seller hereby guarantees that these diamonds are conflict free, based on personal knowledge and/or written guarantees provided by the supplier of these diamonds.”</p>
            
            <p> New definition: </p>
            
            <p> “The diamonds herein invoiced have been {sourced}* purchased from legitimate sources not involved in funding conflict, in compliance with United Nations Resolutions and corresponding national laws {where the invoice is generated}**. The seller hereby guarantees that these diamonds are conflict free and confirms adherence to the WDC SoW Guidelines.”</p>
            
            <p> Apart from adhering to the KPCS, those who would want to voluntarily adopt the new SoW Guidelines have to implement universally accepted principles covered by the following internationally accepted documents:</p>
            
             <ul class="inner_under_listing">
            	<li>UN Guiding Principles on Business and Human Rights</li>
<li>ILO Declaration on Fundamental Principles and Rights at Work</li>
<li>UN Convention Against Corruption</li>
<li>AML Guidelines as per National Laws in line with FAFT 40 Recommendations on Money Laundering for Dealers in Precious Metals and Stones</li>

            </ul>

            <p> Buyers who buy from artisanal miners need to promote the implementation of the intergovernmental Forum on Mining, Minerals, Metals and Sustainable Development “Mining Policy Framework”.</p>
            
            <p> <strong> Key Points of New SoW Declaration </strong> </p>
            
            <p> Adopting new SoW Guidelines (and declaration) is voluntary. </p>
            
            <p> To be able to give the WDC SoW declaration, a company needs: </p>
            
            <ul class="inner_under_listing">
            
            <li> To receive the new warranty on all its purchases of rough diamonds, polished diamonds and jewellery containing diamonds </li>
            
            <li> A transition period has been provided, where you can accept the old SoW declaration from your suppliers (start date possibly January 1st 2020) </li>		
            
            <li> 3 years for purchase of rough diamonds </li>
            
            <li> 5 years for purchase of polished diamonds and diamond jewellery </li>
            
           <li> Annual reconciliation of the SoW declarations received by your auditor</li>
<li> To complete, maintain and share (when requested) a Self-Assessment indicating their adherence to human and labour rights, AML and anti-corruption measures (Toolkit)</li>
<li> Self-assessment to be done on an annual basis</li>
<li> All laws referred to are national laws</li>
</ul>
            
	
