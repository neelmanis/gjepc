                        <p><strong>28. What are the RBI guidelines on interest rates for loans disbursed by  the commercial banks?</strong></p>
                        <p>As part of the financial  sector liberalization, all credit related matters of banks including charging  of interest have been deregulated by RBI and are governed by the banks' own  lending policies.</p>
                        <p>With a view to enhancing transparency in lending rates of banks and  enabling better assessment of transmission of monetary policy, all scheduled  commercial banks had been advised in terms of RBI circular;</p>
                        <p>DBOD.No.Dir.BC.88/13.03.00/2009-10on April 9, 2010 to introduce the Base  Rate system w.e.f. July 1, 2010. Accordingly, the Base Rate System has replaced  the BPLR (Bank&rsquo;s prime Lending Rate) system with effect from July 1, 2010. All  categories of loans should henceforth be priced only with reference to the Base  Rate.</p>
