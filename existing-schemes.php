            		<h2 class="title">D. Credit Link Capital Subsidy Scheme for Technology Upgradation </h2>

                  <p><strong> Objective : </strong> Technology support/ Credit Support </p>
    
                  <p><strong>Key Benefits :</strong></p>
    
                  <ul class="mb-3 inner_under_listing">
                        <li>Induction of established and improved technologies.  </li>
                        <li>15% Subsidy on institutional credit upto Rs 1.00 crore with a subsidy cap of Rs.15.00 lakh.</li>
                  </ul> 
                 
                 <p><strong>Scheme applicable for </strong></p>
    
                 <p>Existing Entrepreneurs & Aspiring Entrepreneurs </p>
    
                  <p><strong>Detailed Information  </strong></p>
    
                  <p>The objective of CLCS component of CLC-TUS is to facilitate technology to MSEs through institutional finance for induction well established and proven technologies in the specific sub-sector/products approved under the scheme: </li>
                
                <ul class="inner_under_listing">
                
                  <li>Upfront subsidy of 15% on institutional credit up to Rs. 1.0 crore (i.e. a subsidy cap of Rs. 15.00 lakh) for identified sectors/subsectors/ technologies. </li>
    
                  <li>Flexibility for review of the identified technologies/ subsectors also exists. </li>
    
                  <li>Presently scheme is being implemented through 11 nodal banks/agencies, however, almost all Commercial Banks, Pvt. Banks & RRBs are acting as PLI through these 11 nodal banks/agencies {SIDBI, NABARD, SBI, Andhra Bank, PNB, BoB, Canara Bank, Corporation Bank, Indian Bank, Bank of India, & TIICL}. </li>
    
                  <li>In order to ensure fair inclusion of SC/ST category, women entrepreneurs and entrepreneurs from NER, Hill States (Jammu & Kashmir, Himachal Pradesh & Uttarakhand) Island Territories (Andaman & Nicobar and Lakshadweep) and the identifies Aspirational Districts/LWE Districts, the subsidy has also been proposed to be admissible also for investment in acquisition/replacement of plant & machinery/equipment & technology up-gradation of any kind. </li>
                  
                  </ul>
    
                  <p><strong>How to apply :</strong> As per Scheme guidelines - Through Prime lending Institution / Nodal Banks.  To know more please visit: <a href="https://clcss.dcmsme.gov.in/" target="_blank"> https://clcss.dcmsme.gov.in/</a></p>
                
