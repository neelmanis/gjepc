                <a class=" collapsed  " data-toggle="collapse" data-target="#collapseR3" aria-expanded="false" aria-controls="collapseR3">
                <i class="fa fa-plus"></i>C.	Trade Receivables Discounting System (TReDS)
                </a>
            </div>
            <div id="collapseR3" class="collapse" aria-labelledby="headingR3" data-parent="#accordion18" >
                <div class="card-body">
                    <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                        <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                            <p><strong>1. What is Bank Credit Facilitation Scheme?</strong></p>
                            <p>To meet the credit requirements of MSME units NSIC has entered into a  Memorandum of Understanding with various Nationalized and Private Sector Banks.  Through syndication with these banks, NSIC arranges for credit support (fund or  non fund based limits) from banks.</p>
                        </div>
                        <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                            <p><strong>1.	What is Trade Receivables Discounting System (TReDS)?</strong></p>
                            <p> The objective of TReDS is to create Electronic Bill Factoring Exchanges which could electronically accept and settle bills so that MSMEs could encash their receivables without delay. This will not only give them greater access to finance but will also put greater discipline on corporates to pay their dues on time. For more details you may refer to RBI guidelines for setting up and operating TReDS on their website https://www.m1xchange.com/treds.php </p>
                        </div>
                        <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                            <p><strong>2.	Who are the participants in TReDS?</strong></p>
                            <p> Sellers, buyers and financiers are the participants on a TReDS platform.</p>
                        </div>
                        <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                            <p><strong>3.	Who can participate as a seller in TReDS?</strong></p>
                            <p> Only MSMEs can participate as sellers in TReDS. </p>
                        </div>
                        <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                            <p><strong>4.	Who can participate as a buyer in TReDS?</strong></p>
                            <p> Corporates, Government Departments, PSUs and any other entity can participate as buyers in TReDS. </p>
                        </div>
                        <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                            <p><strong>5.	Who can participate as a financier in TReDS?</strong></p>
                            <p> Banks, NBFC - Factors and other financial institutions as permitted by the Reserve Bank of India (RBI), can participate as financiers in TReDS. </p>
                        </div>
                        <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                            <p><strong>6.	How does TReDS work?</strong></p>
                            <p> Broadly, following steps take place during financing / discounting through TReDS: 
                                Creation of a Factoring Unit (FU) - standard nomenclature used in TReDS for invoice(s) or bill(s) of exchange - containing details of invoices / bills of exchange (evidencing sale of goods / services by the MSME sellers to the buyers) on TReDS platform by the MSME seller (in case of factoring) or the buyer (in case of reverse factoring); 
                                Acceptance of the FU by the counterparty - buyer or the seller, as the case may be; 
                                Bidding by financiers; 
                                Selection of best bid by the seller or the buyer, as the case may be; 
                                Payment made by the financier (of the selected bid) to the MSME seller at the agreed rate of financing / discounting; 
                                Payment by the buyer to the financier on the due date.
                            </p>
                        </div>
                        <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                            <p><strong>7.	What is a Factoring Unit (FU)?</strong></p>
                            <p> A Factoring Unit (FU) is a standard nomenclature used in TReDS for invoice(s) or bill(s) of exchange. Each FU represents a confirmed obligation of the corporates or other buyers, including Government Departments and PSUs. </p>
                        </div>
                        <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                            <p><strong>8.	 Who can create a FU? </strong></p>
                            <p> In TReDS, FU can be created either by the MSME seller or the buyer. If MSME seller creates it, the process is called factoring; if the same is created by corporates or other buyers, it is called as reverse factoring. </p>
                        </div>
                        <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                            <p><strong>9.	Whether TReDS could deal with reverse factoring?</strong></p>
                            <p> Yes. The TReDS could deal with both receivables factoring as well as reverse factoring. </p>
                        </div>
                        <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                            <p><strong>10.	Whether the MSME seller would have to pay to the financier in case the buyer defaults in repayment?</strong></p>
                            <p> No. The transactions processed under TReDS are "without recourse" to the MSMEs. </p>
                        </div>
                        <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                            <p><strong>11.	Whether any authorization is required to set up and operate a TReDS platform?</strong></p>
                            <p> Yes, authorization is required to be obtained from RBI under the Payment and Settlement Systems (PSS) Act, 2007. </p>
                        </div>
                        <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                            <p><strong>12.	What is the eligibility criteria for setting up and operating TReDS?</strong></p>
                            <p> Eligibility criteria for the purpose of setting up and operating a TReDS platform is provided in the guidelines (as amended from time to time) for TReDS issued by RBI. These guidelines are available at the following path: www.rbi.org.in →"Payment and Settlement Systems" dropdown →"Guidelines". RBI's Press Release dated October 15, 2019 may also be read in this regard. The same can be accessed at the following web links: Click here and Click here </p>
                        </div>
                        <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                            <p><strong>13.	Where can I find the details of TReDS entities authorised by RBI?</strong></p>
                            <p>List of all authorized Payment System Operators (PSOs), including TReDS, is available at the following path: www.rbi.org.in →"Payment and Settlement Systems" dropdown →"Information Useful to Customer"→"List of Authorised Entities – Payment System Operators". </p>
                        </div>
                        <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                            <p><strong>14.	Whether TReDS entities undertake KYC (Know Your Customer) of participants?</strong></p>
                            <p>Yes. The KYC process adopted by the TReDS entities shall adhere to the "Master Direction – Know Your Customer (KYC) Direction, 2016" dated February 25, 2016 (as amended from time to time) issued by RBI. </p>
                        </div>
                        <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                            <p><strong>15.	What is a settlement file and who generates it in TReDS?</strong></p>
                            <p> A settlement file provides information as to how much amount has to be debited from and credited to the accounts of participants (sellers, buyers and financiers), due on a date / time. In other words, it indicates how much a financier has to pay to an MSME seller, and how much a buyer owes to the financier on a date / time. The TReDS entities generate the settlement file and send the same to existing payment systems (for instance, National Automated Clearing House) for actual payment of funds. </p>
                        </div>
                        <div>
                            <p><strong>16.	Whether defaults on TReDS platform are the responsibility of TReDS entities?</strong></p>
                            <p> No. Default handling is outside the purview of TReDS platforms. </p>
                        </div>
