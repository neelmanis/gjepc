                        <p>Scheduled commercial banks  have been advised in terms of our circular RPCD.SME&amp;NFS.  BC.No.102/06.04.01/2008-09 dated May 4, 2009 to put in place a non  -discretionary one time Settlement scheme duly approved by their Boards. The  banks have also been advised to give adequate publicity to their OTS policies.  (Refer circular RPCD.SME&amp;NFS. BC.No.102/06.04.01/2008-09 dated May 4, 2009)</p>
