                                    <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                                    <p> <strong> 1. What is the Credit Guarantee Scheme for MSEs (CGTMSE)? And  What is the support available for collateral free borrowing </strong> </p>
                                    
                                    <p> The Ministry of MSME, Government of India and SIDBI set up the Credit Guarantee Fund Trust for Micro and Small Enterprises (CGTMSE) with a view to facilitate flow of credit to the MSE sector without the need for collaterals / third party guarantees. </p>
                                    <p>The main objective of the scheme is that the lender should give importance to project viability and secure the credit facility purely on the primary security of the assets financed. The Credit Guarantee scheme (CGS) seeks to reassure the lender that, in the event of a MSE unit, which availed collateral - free credit facilities, failing to discharge its liabilities to the lender, the Guarantee Trust would make good the loss incurred by the lender up to 85 per cent of the outstanding amount in default. </p>
                                    <p> The CGTMSE would provide cover for credit facility up to Rs. 200 lakh which have been extended by lending institutions without any collateral security and /or third-party guarantees. A guarantee and annual service fee is charged by the CGTMSE to avail of the guarantee cover. </p>
    
                    <p><strong>Revised Scheme:</strong> Financial assistance on already existing schemes Upto 20% of entire Outstanding credit as on 29.2.2020 for Units upto Rs. 100 cr. turnover eligible. No guarantee fee, no fresh collateral.</p>
    
                    <p>For more details you may visit <a href="www.cgtmse.in." target="_blank" class="gold_clr"><strong>www.cgtmse.in</strong></a></p>
                    	
                        </div>
                        
                          <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
              		<p> <strong>	2. Who can apply for assistance under this scheme? </strong> </p>
                
                    <p> Any MSME registered unit in Manufacturing or Service sector can apply for assistance under this scheme. </p>
                    </div>
                
                <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
               <p> <strong> 3. Whether borrowers from all service sector enterprises are eligible under the Scheme? </strong> </p>
               As of now, all activities that come under service sector as per MSMED Act, 2006 except retail trade are eligible for coverage under the scheme.</p>
               </div>
               
               <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
              <p> <strong> 4. Which type of borrowers can be covered under the Scheme? </strong> </p>
                 
                    <p> New and existing Micro and Small Enterprises engaged in manufacturing or service activity excluding Retail Trade, Educational Institutions, Agriculture, Self Help Groups (SHGs), Training Institutions etc. </p>
                    </div>
              
              <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
              <p> <strong>5. Which are considered as eligible lending institutions under the scheme? </strong> </p>
                
                    <p>All Scheduled Commercial Banks (either PSU, Private or Foreign Banks), selected Regional Rural Banks, selected state financial corporations or such of those institutions as may be directed by GOI can avail of guarantee cover in respect of their eligible credit facilities under the Scheme.</p>
                    <p> Small Industries Development Bank of India (SIDBI), National Small Industries Corporation Ltd. (NSIC) and North Eastern Development Finance Corporation Ltd. (NEDFi) have been included as eligible institutions. </p>
                    
</div>
           
           <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
             <p> <strong>6. What is quantum of credit facility that can be covered under the Scheme? </strong> </p>
               
                    <p>Fund and non-fund based (Letters of Credit, Bank Guarantee etc.) credit facilities up to Rs. 100 lakh per eligible borrower are covered under the guarantee scheme provided they are extended purely on the project viability without collateral security or third party guarantee.</p>
                    </div>
                    
                    <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
               <p> <strong>7. Can a Private sector bank or a foreign bank be eligible for guarantee cover? </strong> </p>
                 
                    <p>Yes, provided it is a commercial bank listed in the II Schedule to the Reserve Bank of India Act, 1934. </p>
                    </div>
                    
                    <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                <p> <strong>8. Is Regional Rural Bank eligible for guarantee cover?  </strong> </p>
                 
                    <p>Yes, fulfilling the criteria of circular dated June 10, 2014 on "Review of Interest cap and other registration criteria of Regional Rural Banks (RRBs) as Member Lending Institutions (MLIs) of CGTMSE". </p>
                    <p>The Trust shall cover credit facilities (Fund based and/or Non fund based) extended by select RRB(s) to a single eligible borrower in the Micro and Small Enterprises sector for credit facility not exceeding 50 lakh by way of term loan and/or working capital facilities on or after entering into an agreement with the Trust, without any collateral security and/or third party guarantees.</p>
                    </div>
                    
                    <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
               <p> <strong>9. Is guarantee benefit available to existing units of a lending institution which has become a MLI of CGTMSE? </strong> </p>
                
                    <p> In case of existing units, additional credit facilities in the form of term loan or renewal of working capital facilities can be covered as and when the facilities are extended, provided no collateral security and/ or third party guarantee is obtained. </p>
                    <p>Part of the credit facility with collateral and part of the facility without collateral for guarantee cover would not be entertained if it is considered as a composite credit.</p>
                    </div>
                    
                    <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
              <p> <strong>10. Is it possible to cover credit facilities, which have already become NPA? </strong> </p>
               
                    
                    <p>No, the credit facility that has already become NPA cannot be covered under the Scheme.</p>
                    </div>
                    
                    <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
          <p> <strong>  11. What would be the guarantee / service fee that would be payable by the member-lending institution on credit facility sanctioned in excess of 200 lakh? </strong> </p>
                 
                    <p>Presently, annual guarantee fee is payable @1% (0.75% or 0.85% in case of Women, Micro and units in North Eastern Region including state of Sikkim) on the credit facility agreed to be covered by the Trust. </p>
                    <p>In this case, maximum of 200 lakh would be extended guarantee cover even though the sanctioned amount exceeds 200 lakh. </p>
                    </div>
                    
                     <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">                           
            <p> <strong> 12. Can a credit facility extended to a borrower against a collateral security be covered under the Guarantee Scheme, if the lending institution relinquishes its rights on the collateral security? </strong> </p>
              
                    <p> Yes, provided the lending institution relinquishes its rights on the collateral assets and releases the same in favour of the borrower before seeking guarantee cover and subject to fulfilment of the other norms of the Scheme.</p>
                    <p> Further, in case the MLIs has to retain the collateral security for the existing credit facility, a new credit facility extended to same borrower, without taking collateral can be covered under the scheme provided, the MLI is not extending the charge on the existing collateral to new facility. </p>
                    </div>
                    
    <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
           <p> <strong>  13. Can term loan or working capital facility alone be extended by an eligible lender and still be covered under the guarantee scheme? </strong> </p>
                
                    <p> Yes, a lender can extend either term loan or working capital facility alone and still be eligible for a guarantee cover if it meets the other eligibility parameters.</p>
                    <p> Needless to say, the credit facility extended to a borrower should be without any collateral security and/or third party guarantee.  </p>
                 
                 </div>
                 
                 <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
              <p> <strong>   14. Is there any ceiling in respect of interest to be levied on the credit facility advanced to the borrower if the same is to be covered under the Scheme?  </strong> </p>
                
                    <p> Any credit facility which has been sanctioned by the lending institution on or after September 01, 2015 with interest rate more than 2% and 3% over the Base Rate/ (Average Base rate decided by the Trust from time to time as applicable to RRBs) for loans up to 50 lakh and loans above 50 lakh respectively. </p>
                    <p>However, in case of those MLIs for whom Reserve Bank of India has not enjoined Base Rate concept, the existing provision of CGS will continue.  </p>
                    
                    </div>
                    
                    <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
               <p> <strong> 15. When will the guarantee cover commence for the eligible credit facility? </strong> </p>
                 
                    <p> The guarantee cover will commence from the date on which guarantee fee proceeds are credited to bank account of the Trust. </p>
                    
                    </div>
                    
                    <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                    
             <p> <strong>  16. Does CGTMSE have branch offices in other cities in India?  </strong> </p>
                 
                    <p>CGTMSE has its Registered Office at Mumbai and does not have any branches. Since the entire operations are online, CGTMSE is able to cater to the needs of its MLIs from Mumbai. </p>
                    
                    </div>
                       
                       <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                        
             <p> <strong> 17. How is this guarantee scheme operated by the Trust?   </strong> </p>
                
                    <p>The operations of this Scheme are fully computerized using B2B e-business concept to enable the Trust to deliver prompt service to the lenders.</p>
                    
                    </div>
                    
                    <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
               <p> <strong> 18. How long the guarantee cover is available for credit facilities extended to a particular borrower?   </strong> </p>  
                  
                    <p>Guarantee will commence from guarantee start date and shall run through the agreed tenure of the term loan / composite loans. Where working capital facilities alone are extended to eligible borrowers, it would be for a period of 5 years or block of 5 years on renewal of the guarantee cover, provided MLI pays the Annual Service Fee due as on March 31, latest by within 60 days from the date of demand by CGTMSE.</p>
                    
                    </div>
                    
                    <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                 <p> <strong> 19. What is the tenure of the cover for credit relating to working capital?  </strong> </p>
                           
                                <p>The tenure for coverage of working capital facilities is 5 years, where working capital alone is covered under the scheme. In case term credit and working capital both are covered under the scheme, the tenure relating to working capital facility would match the normal repayment period of term credit. </p>
                                <p>The reason for keeping a limit of 5 years wherever working capital alone are covered are that the period for which the same are extended by the lending institutions are not time bound. </p>
                                <p>The same are reviewed periodically for increase/ decrease in the limit sanctioned, and are expected to continue for a time frame much longer than 5 years. CGTMSE welcomes any renewal of guarantee cover beyond 5 years on a payment of applicable guarantee fee.</p>
             	</div>
                
             <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
             <p> <strong> 20. Can the Micro and Small Entrepreneurs / borrowers approach the Trust directly to seek guarantee for the credits sanctioned by the Banks?  </strong> </p>   
                
                    <p> Entrepreneurs in the Micro and Small Enterprises sector have to approach the Banks or financial institutions (who have already registered with the Trust as MLIs) with their viable proposals for their credit requirements. The List of MLIs of the Trust can be seen at CGTMSE's web-site at <a href="http://www.cgtmse.in" target="_blank" class="gold_clr"><strong>http://www.cgtmse.in</strong> </a> or <a href="www.cgtsi.org.in" target="_blank" class="gold_clr"><strong>www.cgtsi.org.in</strong></a></p>
                    </div>
                    
                    <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
               <p> <strong> 21. What information is required to be submitted to the Trust before starting operations under the Scheme? </strong> </p>
                 
                    <p>The lending institution has to submit an undertaking (download from <a href="#" target="_blank" class="gold_clr"><strong>http://www.cgtmse.in/files/Undertaking.pdf</strong></a>) on a stamp paper of appropriate value to the registered office of the Trust. Subsequently, MLIs are required to give the names and addresses of the Zonal / Regional / Branch offices through which they would like to operate the Scheme. </p>
                    <p>They are also required to furnish the names of a nodal officer and two other officers who will be operating the Scheme at each of the operating offices. On receipt of this information, Member ID and Passwords would be allotted by the Trust, after which it would be possible for MLIs to lodge applications for guarantee cover. Applications are required to be lodged online.</p>
                    </div>
                    
