                        <p><strong>30. What is debt restructuring of advances?</strong></p>
                        <p>A viable/potentially viable  unit may apply for a debt restructuring if it shows early stage of stickiness.  In such cases the banks may consider to reschedule the debt for repayment,  consider additional funds etc. A debt restructuring mechanism for units in MSME  sector has been formulated and advised to all commercial banks. The detailed  guidelines have been issued to ensure restructuring of debt of all eligible  small and medium enterprises. Prudential guidelines on restructuring of  advances have also been issued which harmony the prudential norms over all  categories of debt restructuring mechanisms (other than those restructured on  account of natural calamities). </p>
                        <p> The relevant circulars in this regard are  circular DBOD.BP.BC.No.34/21.04.132/2005-06 dated September 8, 2005 and  circular DBOD.No.BP.BC.37/21.04.132/2008-09 dated August 27, 2008 which are  available on our website <a href="https://www.rbi.org.in/" target="_blank" class="gold_clr"> <strong> www.rbi.org.in </strong> </a> </p>
                    </div>
                    <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                        <p><strong>31. What is the definition of a sick unit?</strong></p>
                        <p>As per the extant guidelines,  a Micro or Small Enterprise (as defined in the MSMED Act 2006) may be said to  have become Sick, if</p>
                        <p>Any of the borrower account of the enterprise remains NPA for three months  or more.</p>
                        <p>OR</p>
                        <p>There is erosion in the net worth due to accumulated losses to the extent  of 50% of its net worth during the previous accounting year. This criterion  enables banks to detect sickness at an early stage and facilitate corrective  action for revival of the unit.</p>
                    </div>
                    <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                        <p><strong>32. Are all sick units put under rehabilitation by banks?</strong></p>
                        <p>No. If a sick unit is found  potentially viable it can be rehabilitated by the banks. The viability of the  unit is decided by banks. A unit should be declared unviable only if such a  status is evidenced by a viability study.</p>
                    </div>
                    <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                        <p><strong>33. Is there a time frame within which the banks are required to implement  the rehabilitation package?</strong></p>
                        <p>Viable / potentially viable  MSE units/enterprises, which turn sick in spite of debt re-structuring, would  need to be rehabilitated and put under nursing. It will be for the  banks/financial institutions to decide whether a sick MSE unit is potentially  viable or not. The rehabilitation package should be fully implemented by banks  within six months from the date the unit is declared as potentially  viable/viable. During this six months period of identifying and implementing  rehabilitation package banks/FIs are required to do &ldquo;holding operation&rdquo; which  will allow the sick unit to draw funds from the cash credit account at least to  the extent of deposit of sale proceeds. The relevant circular on rehabilitation  of sick units is RPCD.CO.MSME &amp; NFS.BC.40/06.02.31/2012-2013 dated November  1, 2012 is available on our website.</p>
                    </div>
                    <div style="border-bottom: 1px solid #ddd; margin-bottom: 15px;">
                        <p><strong>34. What is the procedure and time frame for conducting the viability  study?</strong></p>
                        <p>The decision on viability of  the unit should be taken at the earliest but not later than 3 months of the  unit becoming sick under any circumstances. The following procedure should be  adopted by the banks before declaring any unit as unviable:</p>
                        <p>A unit should be declared unviable only if the viability status is  evidenced by a viability study. However, it may not be feasible to conduct  viability study in very small units and will only increase paperwork. As such  for micro (manufacturing) enterprises, having investment in plant and machinery  up to Rs. 5 lakh and micro (service) enterprises having investment in equipment  up to Rs. 2 lakh, the Branch Manager may take a decision on viability and  record the same, along with the justification.</p>
                        <p> The declaration of the unit as  unviable, as evidenced by the viability study, should have the approval of the  next higher authority/ present sanctioning authority for both micro and small  units. In case such a unit is declared unviable, an opportunity should be given  to the unit to present the case before the next higher authority. The  modalities for presenting the case to the next higher authority may be worked  out by the banks in terms of their Board approved policies in this regard the  next higher authority should take such decision only after giving an  opportunity to the promoters of the unit to present their case. For sick units  declared unviable, with credit facilities of Rs. 1 crore and above, a Committee  approach may be adopted.</p>
                        <p> A Committee comprising of senior officials of the bank  may examine such proposals. This is expected to improve the quality of  decisions as collective wisdom of the members shall be utilized, especially  while taking decision on rehabilitation proposals. The final decision should be  communicated to the promoters in writing. The above process should be completed  in a time bound manner and should not take more than 3 months.</p>
                    </div>
