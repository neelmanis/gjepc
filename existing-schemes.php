                <p> The Ministry implements the "Technology and Quality Upgradation Support to Micro, Small and Medium Enterprises (TEQUP)" which focuses on two important aspects, namely, enhancing competitiveness of MSME sector through Energy Efficiency and Product Quality Certification. The basic objective of this scheme is to encourage MSMEs in adopting energy efficient technologies and to improve product quality of manufacturing in MSMEs. It is a well-known fact that energy consumption is a significant component in the cost structure of almost any manufacturing/ production activity. Adopting energy efficient technologies curtails the cost of energy there by reducing production cost and increasing competitiveness. Under this scheme, a capital subsidy of 25% of the project cost subject to a maximum of Rs. 10.00 lakh shall be provided to the registered MSME units. While 25% of the project cost will be provided as subsidy by the Government of India, the balance amount is to be funded through loan from SIDBI/banks/financial institutions. The minimum contribution as required by the funding agency will have to be made by the MSME unit. For further details please visit <a href="visit www.dcmsme.gov.in" target="_blank" class="gold_clr"> <strong>  www.dcmsme.gov.in</strong> </a></p>
                
