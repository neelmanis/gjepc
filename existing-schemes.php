                        <p>Financial Literacy and consultancy support:</p>
                        <p>Banks have been advised to either separately set up special cells at their  branches, or vertically integrate this function in the Financial Literacy  Centres (FLCs) set up by them, as per their comparative advantage. Through  these FLCs, banks provide assistance to the MSE entrepreneurs in regard to  financial literacy, operational skills, including accounting and finance,  business planning etc. (Refer circular RPCD.MSME &amp;  NFS.BC.No.20/06.02.31/2012-13 dated August 1, 2012)</p>
                        <p>Further, with a view to providing a guide for the new entrepreneurs in this  sector, a booklet titled &ldquo;Nurturing Dreams, Empowering Enterprises – Financing  needs of Micro and Small Enterprises – A guide&rdquo; has been launched on August 6,  2013 by the Reserve Bank. The booklet has been placed on our website  www.rbi.org.in under the following path &amp; URL:</p>
                        <p>RBI main page – Financial Education – Downloads – For Entrepreneurs  (http://rbi.org.in/financialeducation/FinancialEnterprenure.aspx)</p>
