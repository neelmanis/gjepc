                        </a>
                    </div>
                    <div id="collapseC2" class="collapse" aria-labelledby="headingC2" data-parent="#accordion3">
                        <div class="card-body mb-4">
                            <p>Given the need to orient potential migrant workers with regards to language, culture, do's and don'ts in the destination country, the emigration process and welfare measures, PDOT program has been launched. Ministry of External Affairs (MEA) in collaboration with Ministry of Skill Development and Entrepreneurship (MSDE) is conducting the PDOT program. NSDC is the implementing agency for this program.</p>
                            <p>A longer variant of PDOT i.e. 160 hours was offered at all IISCs which consisted of country orientation, language and digital literacy.</p>
                            <p>A shorter variant of PDOT program i.e. 1 Day (ongoing) is offered to all migrant workers who are likely to depart soon and register for the training through registered recruitment agents.</p>
                            <p>PDOT program is delivered by trainers who have undergone Training of Trainers (ToT) program organized by MEA. So far, 52 trainers from existing IISCs and NSDC Training Partners have undergone the PDOT (ToT).</p>
                            <p>PDOT Training of Trainer (TOT) Summary-Conducted at MEA from 27-31 March 2017 at Pravasi Bharatiya Kendra, New Delhi.</p>
                            <p>The one-day PDOT program was piloted in 2 locations of Mumbai and Delhi each and commenced on 10th January 2018. Since inception, over 23,700 candidates have received training (as of 4th October 2018) under One-Day PDOT Program.</p>
                            <p>In the near future, this program is expected to be scaled up to include five more cities namely Hyderabad, Cochin, Chennai, Kolkata, and Lucknow.</p>


