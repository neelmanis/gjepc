            <h2 class="title">Skill Development and Entrepreneurship: Policy</h2>
            <p>Government framed the National Policy for Skill Development and Entrepreneurship 2015. The primary objective of this policy was to meet the challenge of skilling at scale with speed, standard (quality) and sustainability.</p>

            <div class="newAccordion" id="accordion3">
                
                <div class="card mb-2">
                    <div class="card-header" id="headingC1">
                        <a class="" data-toggle="collapse" data-target="#collapseC1" aria-expanded="false" aria-controls="collapseC1">
                        <i class="fa fa-plus"></i>India International Skill Centre (IISC)
                        </a>
                    </div>
                    <div id="collapseC1" class="collapse" aria-labelledby="headingC1" data-parent="#accordion3">
                        <div class="card-body mb-4">
                            <p><strong>Pilot closed as on 2nd July 2018</strong></p>
                            <p>India International Skill Centres (IISCs) were set up through the National Skill Development Corporation (NSDC) with the objective to provide skill training and certification bench marked to International Standards to facilitate overseas mobility of Indian workforce for jobs.</p>
                            <p>Ministry of Skill Development and Entrepreneurship (MSDE) provided support for domain training and Ministry of External Affairs (MEA) is providing support for Pre-Departure Orientation Training (PDOT) under the Pravasi Kaushal Vikas Yojna (PKVY) framework.</p>
                            <p>In the IISC pilot phase, 14 centres were operationalized, and 593 candidates(offline data) were enrolled at these centres. The assessment and certification under IISC pilot were done by an International Awarding Body.</p>


