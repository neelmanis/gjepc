				<h2 class="title mt-4 mb-2">Unsuccessful Previous Attempt</h2>
    
    			<p> The Government of India introduced the Gold Monetisation Scheme (GMS) in 2015 in an endeavour to unlock this idle gold. The scheme had limited objectives for cutting imports and mobilising funds for the Government by selling gold. </p>
    
				<p>It is financially unviable, since the effective interest cost to the Government amounts to 11.5% per annum, considering the price inflation of gold @9% per annum while redeeming the deposit at market price.</p>

				<p>Plus, the scheme has low incentives for the banks due to high interest and inflation cost on idle gold.</p>

				<p>Depositors, too, are afraid of the 'tax hazards', as most of the gold is acquired through inheritance and gifts, making it difficult to produce supporting purchase invoices. To add to this, testing and melting charges are borne by depositors. In addition, the depositor can't liquidate the investment, if needed.</p>

				<h2 class="title mt-4 mb-2"> GMS 2020 Proposal</h2>

				<p>The GJEPC has proposed certain modifications to the Gold Monetisation Scheme for the Government to consider while drafting the modified GMS to bring more idle gold into circulation.</p>

<p>Colin Shah, Chairman, GJEPC, said, "Cutting down on imports of gold substantially can be a game changer for the economy. Currently, gold imports cause a trade deficit of 2.1% of nominal GDP. The new modified GMS will be a win-win for all the stakeholders involved – gold depositors (customers), banks, RBI and the Government. I am hopeful that the modified GMS will be helpful in unearthing some of the estimated 25,000 tonnes of gold stored in households across India. This would be another step towards making India self-reliant when it comes to gold."</p>

<p> Vipul Shah, vice chairman, GJEPC, said, "The modified GMS will bring further transparency into the gold trade. Its success will fundamentally alter the manner in which gold is bought, held, used, pledged and sold in the future." </p>

<p><strong>Here are the modifications to the Gold Monetisation Scheme suggested by the GJEPC:</strong> </p>

<ul class="inner_under_listing">


<li>Retailers can acquire gold jewellery from customers and after deducting the soldering, stones, etc. the retailer should confirm the pure gold weight in terms of 995.</li>
<li>Once a customer agrees to the deal, the retailer should procure the customer's KYC details (Aadhaar Card, address proof, mobile number, etc.) and take possession of the gold.</li>
<li>The retailer should then give the Reserve Bank of India (RBI) certified gold bought from the authorised dealer,  Government of India Mint gold, or India Good Delivery Gold Bar in denominations of 100gms, 500gms, or 1,000gms along with the certificate and details of the customer for a deposit period of 3 or 5 or 8 years, depending on the customer's wish. The same transaction can also be done with digital gold.</li>
<li>RBI can fix the compounding interest rate accordingly, for example, at 1.5% for 3 years, at 2% for 5 years, or 2.5% for 8 years and so on.</li>
<li>On maturity of the deposit period, whatever compounding interest is accrued should be added in terms of gold with the deposited quantity.</li>
<li>RBI can give the gold deposited by customers to the banks instead of the banks getting gold loans from foreign suppliers. RBI can charge interest for it accordingly.</li>
<li>Presently, as per the income tax act, a married woman can own up to 500gms of gold, an unmarried lady up to 250gms of gold and a male family member is allowed up to 100gms, for which no questions are asked. The government can increase the same limits to 750gms to 1kg of gold for a married woman; 300gms to 500 gms for an unmarried woman; and 200gms to 300gms for a male family member. If these limits are maintained under the GMS scheme, no GST or capital gains tax should be charged.</li>
<li>	On maturity, the retailer who has deposited the gold should produce an authorisation letter from the customer, and collect the gold in denominations of 100gms, or 500gms, or 1kg or multiples thereof. This can also be India Good Delivery gold.</li>

