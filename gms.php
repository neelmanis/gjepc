            		<p class="text-justify">India imports on average about 900 tons of gold valued around Rs.4.0 trillion each year, resulting in a trade deficit of 2.1% of nominal GDP. Out of the imported 900 tons of gold, about 300 tons cater to investment needs, which are viewed as non-productive. The balance 600 tons is consumed in jewellery form for domestic needs and exports.</p>
                    
