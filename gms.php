<h2 class="title mt-4 mb-2">Win-Win For All</h2>

<p>While the new, proposed GMS hopes to unlock unused gold, it will not only benefit the consumer, retailer and banks, but the nation as well. By treating the gold deposit as a part of the Forex reserve, the scheme will reduce gold imports, improve the sovereign rating, and arrest the rupee's depreciation.</p>

<p>K. Srinivasan, convener of the gold jewellery panel, GJEPC, is optimistic about the success of the proposed GMS: "If retailers are given the responsibility of processing gold deposits from customers, the scheme will be a success. Jewellers have the ability to minimise the loss when melting gold and that will be an added advantage for the customer. These gold deposits will not attract GST or capital gains tax. If all the suggestions by GJEPC are accepted, I expect gold deposits of around 300 tons to be released in the first year itself."</p>

