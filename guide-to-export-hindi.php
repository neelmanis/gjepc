<li>सोने की डिलीवरी बुकिंग के 150 दिनों के भीतर या निर्यात के 30 दिनों के भीतर, जो भी बाद में हो, शेष 80% और सहमत दर पर ब्याज के भुगतान पर लें।</li>
<li>क्रेडिट अवधि के भीतर ईपी कॉपी, सीमा शुल्क प्रमाणित चालान और बैंक वसूली प्रमाणपत्र (बीआरसी) जमा करें।</li>
