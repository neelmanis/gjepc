    <p>Our country has over 120 million tribals living in approx. 135,000 villages. There are 537 different tribes and Sub-tribes, each having their own unique and beautiful culture, language, costume, food habits, thinking etc. Being poor and illiterate these innocent tribals have been exploited for hundreds of years.</p>
    
