        	<div class="topic_head">TentativeTopics:-</div>-->
			
		<p>GJEPC, the apex Gem & Jewellery body of India is organizing the first ever 'India Gold & Jewellery Summit' on 23rd & 24th November, 2018 at JW Marriott New Delhi Aerocity.</p>

<p>The Convention will witness the congregation of senior representatives of Gold Miners, Policy Makers, Retailers, Bankers, Analysts amongst others from across the globe on a single platform to share their opinion on the current status and challenges of the Indian bullion industry both nationally and internationally.</p>
<p>The objectives of the Summit are as below :</p>
<ul>
<li>To bring all the stakeholders across the world who will attend this conference and contribute to defining the policy which can usher the transparency in gold & consequently the jewellery business in India.</li>
<li>To seize the leadership in gold in the world being the largest consumer of gold</li>
<li>To understand the implementation procedures and hiccups from other countries in the world like Turkey, UK, China etc.</li>
<li>To have a discussion amongst Indian and world stakeholders how to utilize gold in enhancing India's objective of being the largest exporters of gold jewellery in the world  and generate 8 million employment by the year 2025</li>
</ul><br>

<p>The Summit will focus on four broad topics namely <strong>Vision 2022: Indian Jewellery Exports At $25 Billion, Code Of Conduct & Standards For Industry, Spot Gold Exchange For India, Value Addition Through Jewellery Manufacturing.</strong></p>

