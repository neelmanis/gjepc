                	<h2 class="title mb-4">What is a Common Facility Centre (CFC)?</h2>
                    
                   <p class="mb-4">A Common Facility Centre is a pool of high-end and capital-intensive, state-of-the-art machinery/equipment, offering various services at affordable rates, especially beneficial to the micro, small and medium enterprises (MSME) sector of the gem and jewellery industry.</p>
                   
                   <h2 class="title mb-4">What is the objective of CFCs?</h2>
             		
                    <div class="two_column">
                    
                    <p>There has been much all-round development with regard to the manufacturing of diamonds and jewellery, especially on the technological front. However, machinery as well as inputs which go into a quality product are often out of reach for the entrepreneurs of the MSME segment, holding back their growth and performance.</p>
                    
                    <p>Small and medium diamond and jewellery manufacturing units located in small towns and interior villages are still operating with the old technology, as the cost to upgrade, acquire and install state-of-the-art technology/equipment is not affordable to them. This has resulted in low productivity and also affected the quality of the finished goods produced. </p>
                    
                    
                    <p>Further, there also exists a need to organise skill upgradation training for artisans involved in diamond manufacturing activities to make them aware about the operation and benefits of using modern  equipment.</p>
                    
                    <p>The establishment of CFCs in small towns and villages would greatly help to transfer the technological advancement benefit to these diamond and jewellery manufacturers.
This would also provide an opportunity to re-skill them on the cutting-edge equipment widely used by the manufacturers globally.</p>
					
