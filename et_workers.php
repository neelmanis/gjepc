            		<p class="text-justify">While on the one hand it is heartening to note that export orders are being placed, on the other hand there is a shortage of skilled workers from West Bengal, Gujarat and Uttar Pradesh, traditionally large sources of labour for the industry, who have left Mumbai due to the pandemic. These workers are either eager to resume, but are unable to do so because of transportation restrictions, or they are reluctant to leave their families in times of crisis. </p>
                    
            		<p class="text-justify">"Even though the times are tough, demand for jewellery exports is seeing an upsurge, and that is a huge positive" says Colin Shah, Chairman, GJEPC. "Similar to every other sector in India, the jewellery industry, too, saw a massive exodus of its workforce amid the nationwide lockdown. Exporters need to reassure their staff that they will be safe in the factories. Today there is enough work, but not enough manpower! It's a tricky situation. While we would all like to see the worker attendance number raised, we may have to temper expectations given the situation." </p>
            
