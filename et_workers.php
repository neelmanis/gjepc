<p>Rahul Dholakia, managing director, Shree Ramkrishna Exports, informs that his company runs only one shift. While there is good demand for their products mainly from the US and China as well as other destinations, he adds, "we cannot carry out even 50% of the demand as the output is only 25% due to the skeleton staff." He adds that even at the Customs division, due to fewer people working there, consignments are being dispatched at a slower pace. Each and every worker of ours hailing from Mumbai or Surat, has been paid since the lockdown was declared in March. The rest of them, who are unable to come to work, are being paid enough to run their households on a monthly basis. Unfortunately, the world is in the grip of the pandemic and it would be inhuman to pressure the workers to come back to factories. They have to come back willingly." </p>








<h2 class="title mt-4 mb-2">Pandemic Precautions</h2>

<p>Almost all companies in SEEPZ, Mumbai, have created stringent safety protocols in-house. As the lifeline of the city - Mumbai's local trains - are not operating, all companies in SEEPZ have arranged private transportation for their employees. Wearing masks is mandatory and the sanitised buses ensure that social distancing is maintained. The entire staff is checked for Covid-19 symptoms. </p>

<p>Within the factories, too, social distancing is being observed; the premises are being sanitised on a daily basis; plexiglass screens between workstations are in place, etc. Factories are maintaining social distancing by leaving every alternate workbench empty.</p>

<p>There's a doctor on-call for 12 hours and there's an isolation room to quarantine anybody showing symptoms of Covid-19. Many companies are sharing intelligence and their experiences in order to follow best practices from each other and implementing it across SEEPZ. Most companies have done a good job to take care of their people during the pandemic.
Shishir Nevatia, Owner, Sunjewels Pvt. Ltd., echoes the general sentiment: "I would like to emphasise that there's a shortage of skilled workers, and it is difficult to get them back because they are afraid to resume as there is a perception that Mumbai is a Covid hotspot. And then, there are transportation issues. Currently, we are working in two shifts with all norms in place, but we are losing orders from our customers who have gone to other countries because we are unable to deliver within their time frame. We are only hoping that after 31st July, the government relaxes the 25% workforce attendance rule to 50%. We have been paying all our employees regularly, especially those earnings Rs.20,000 and below, while those who intend to or have already resumed work are being given an additional incentive. Those who have been incommunicado have been paid up to June." </p>

<p>Many employees who are not physically working on jewellery, like designers, CAD artists, CRM and accounting teams are working from home using conferencing facilities, VPNs for logging into company servers, etc. So even if the percentage of the workforce allowed goes up to 50%, factory owners can comfortably maintain social distancing in their premises.</p> 

<h2 class="title mt-4 mb-2">Production Hurdles</h2>

<p>The Christmas and Holiday Season demand from Western countries accounts for half of a jewellery exporter's annual business, making September-November the busiest period of the year.</p>

<p>Output is also getting bottlenecked as karigars with higher skill sets are missing, leading to more production delays. </p>

<p>More importantly, new product development is suffering significantly because high skilled workers from West Bengal, who are typically model makers, are not available. Many believe that if they are unable to put a new product out there at the same pace, the next round of orders will suffer. </p>

<p>The current export demand is about 50-60% of the same period last year, but most factories are operating at a quarter of their total capacity, thus creating a huge gap between demand and production. </p>

<p>Bobby Kothari, Director, Jewelex India Pvt. Ltd., says, "Demand is not the main concern right now. The Holiday Season orders have already started. We are seeing orders coming from markets that have largely opened up, such as the US, many parts of Europe, Australia and New Zealand. While May to July was a quieter period for exporters, now is when the action starts. We are grateful for the 25% allowance granted so far. We would look forward to 40-50% of the workforce in the coming months."</p>

<p>The products being sourced at the moment are a little more value-oriented. Overseas clients are opting for a lower gold ratio in a piece of jewellery as gold prices have shot up. While others are trading down a little -- instead of full cuts, they're opting for single cuts, a 5 or 6-pointer instead of a 10-pointer diamond. </p>

<p>Online retail is one segment that has stood out during the lockdown period, with e-tailers in Western countries faring better than their offline counterparts, resulting in good demand for export orders in India. </p>

<p>For retailers to sell products between Thanksgiving and Christmas, typically they will need product to land into their warehouses at least two weeks prior, which is sometime early November. For that to happen, orders need to be placed now, so that they can deliver them from September-November. </p>

<p> So if jewellery exporters are not able to get sufficient manpower by August, the whole business volume for the year will get affected drastically.  </p>

                
              
        
