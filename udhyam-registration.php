<li>No enterprise is supposed to file more than one Udyam Registration. However, any number of activities including manufacturing or service or both may be specified or added in one Registration;</li>
<li>Government's Facilitation mechanism in the name of single window systems at Champions Control Rooms and at DICs will help people in this process;</li>
<li>Registration Process is totally free. No Costs or Fees are to be paid in this regard.</li>
<li>Ministry has clarified that except this portal of Government of India <a href="https://udyamregistration.gov.in/" target="_blank" class="gold_clr"> <strong>(www.udyamregistration.gov.in)</strong></a> and Government's Single Window Facilitation Systems, no other private online or offline system, service, agency or person is authorized or entitled to do MSME Registration or undertake any activity related with that process.</li>
<li>Udyam Registration will be an extremely simple, seamless entrepreneur-friendly process which will reduce transaction time and costs and will set an example in Ease of Doing Business, not only in India but internationally too where Entrepreneurs and Enterprises can focus on their real work and become globally competitive.</li>
<li>Facilitation And Grievance Redressal Of Enterprises: The Champions Control Rooms functioning in various institutions and offices of the Ministry of Micro, Small and Medium Enterprises including the Development Institutes (MSME-DI) shall act as Single Window Systems for facilitating the registration process and further handholding the micro, small and medium enterprises in all possible manner. 
The District Industries Centres (DICs) will also act as Single Window facilitation Systems in their Districts. 
MSMEs may visit the below Champions portal with their grievance: <a href="https://champions.gov.in/MyMsme/grievance/Register_Grievance.aspx" target="_blank" class="gold_clr"> <strong> https://champions.gov.in/MyMsme/grievance/Register_Grievance.aspx </strong> </a></li>
