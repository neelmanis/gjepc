<li>There will be no need for renewal of Registration;</li>
<li>PAN & GST linked details on investment and turnover of enterprises will be taken automatically from the respective Government data bases;</li>
<li>MSME Ministry's online system will be fully integrated with Income Tax and GSTIN systems;</li>
