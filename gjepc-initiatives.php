                <p>Jewellery Parks: 
                GJEPC will set a Jewellery Park in Mumbai. Establishment of Jewellery Park will contribute to the economic development of both Maharashtra and India, by facilitating investment, manufacturing, employment of manpower, trade & exports. This dynamic venture will provide effective support to Jewellery manufacturers/traders who are interested to expand or invest in the state of Maharashtra and the region to initiate new business or strengthen their existing business, benchmarking with the Jewellery Park created in countries like China. Turkey, Italy, Thailand, etc.
                 
