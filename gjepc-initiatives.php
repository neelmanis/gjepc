                <p class="blue">Promotional Activities:</p>
                <p>Various promotional activities undertaken by the GJEPC, over the years, have significantly and positively impacted the increase of exports of gems and jewellery from India. 

                The GJEPC’s promotional activities include organising a number of important trade shows held in the country like the India International Jewellery Show (IIJS), Signature IIJS, India Gem & Jewellery Machinery Expo (IGJME).  The IIJS is the largest gems & jewellery fair in the country and amongst the foremost internationally. It attracts visitors and buyers from all across the country and from several countries in the world.  Signature IIJS, a smaller-scale version of the IIJS was set up as a boutique show and has gone on to develop its own niche. IGJME, a trade fair totally devoted to technology, enables companies of this large manufacturing centre to access the cutting edge developments in the field  in a convenient manner. 

                The GJEPC also organises India Pavilions – participation of a number of companies under the GJEPC banner -  at various leading international shows like JCK Las Vegas, Basel, Vicenzaoro,  the Hong Kong Show and several others worldwide.

                Various product-specific Buyer-Seller Meets, delegations to important gems and jewellery centres in other parts of the world, are some of the other trade promotion activities undertaken by the Council.
